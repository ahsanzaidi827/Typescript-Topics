//AIRTHMETIC OPERATORS
var a = 5;
var b = 5;
var result = a + b;
console.log(result);
var x = 10;
console.log(x + 10);
console.log(x - 10);
//COMPARISON OPERATOR
var y = 100;
console.log(y < 90);
console.log(y >= 90);
console.log(y == 100);
console.log(y === 100); //=== this will also check data type of variable
//UNARY OPERATORS
var num1 = 25;
console.log(num1++); //25 Increment
console.log(num1++); //26 Increment
console.log(num1++); //27 Increment
console.log(num1++); //28 Increment
console.log(--num1); //28 Decrement
console.log(--num1); //27 Decrement
console.log(--num1); //26 Decrement
console.log(--num1); //25 Decrement
