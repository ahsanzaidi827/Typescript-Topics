console.log(num1++);//26 Increment
console.log(num1++);//27 Increment
console.log(num1++);//28 Increment

console.log(--num1);//28 Decrement
console.log(--num1);//27 Decrement
console.log(--num1);//26 Decrement
console.log(--num1);//25 Decrement